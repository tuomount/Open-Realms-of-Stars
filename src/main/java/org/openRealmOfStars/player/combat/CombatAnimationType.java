/**
*
* Class for handling combat animation during the combat. Contains
* pixel positions animation frames and so on.
*
*/
public enum CombatAnimationType {
  /**
   * Plasma beam
   */
  PLASMA_BEAM,
  /**
   * Antimatter beam, Light blue laser with light blue particles
   */
  ANTIMATTER_BEAM,
  /**
   * Phasor beam, Green laser with green particles
   */
  PHASOR_BEAM,
  /**
   * Laser beam, red laser with red particles
   */
  LASER_BEAM,
  /**
   * Railgun, blueish railgun effect
   */
  RAILGUN,
  /**
   * ECM Torpedo, ECM missile with smoke trail
   */
  ECM_TORPEDO,
  /**
   * HE Missile, high explosive missle with smoke trail
   */
  HE_MISSILE,
  /**
   * Photon torpedo, green ball with green particles
   */
  PHOTON_TORPEDO,
  /**
   * Privateer logo fading away
   */
  PRIVATEERING,
  /**
   * Lighting sparkling around
   */
  LIGHTNING,
  /**
   * Shield effect
   */
  SHIELD,
  /**
   * Explosion
   */
  EXPLOSION,
  /**
   * Tractor beam
   */
  TRACTOR_BEAM,
  /**
   * Plasma canno
   */
  PLASMA_CANNON,
  /**
   * Ion Cannon
   */
  ION_CANNON,
  /**
   * Scanning
   */
  SCANNING,
  /**
   * BITE
   */
  BITE,
  /**
   * Tentacle
   */
  TENTACLE,
  /**
   * Multicannon
   */
  MULTICANNON,
  /**
   * Gravity Ripper
   */
  GRAVITY_RIPPER,
  /**
   * Spike
   */
  SPIKE,
  /**
   * Plasma spit
   */
  PLASMA_SPIT


}
